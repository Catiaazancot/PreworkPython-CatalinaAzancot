'''Crea un programa que calcule el área de un rectángulo. El usuario debe ingresar la
longitud y el ancho del rectángulo.'''

print("Introduce la longitud del rectangulo: ")
longitud = float(input())
print("Introduce el ancho del rectangulo: ")
ancho = float(input())

area_rectangulo = longitud * ancho 

print("El area del rectangulo es: ", area_rectangulo)

              